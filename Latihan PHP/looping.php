<?php
    //Perulangan for
    for ($i=1; $i<=10; $i++) {
        echo ("Perulangan for ke-$i <br>");
    }

    //Perulangan while (Penentuan nilai awal (inisialisasi))
    $i=1;
    while ($i<= 10) {
        echo ("Perulangan while ke-$i <br>");
        $i++;
    }

    //Perulangan do-while

    $i= 1;
    do{
        echo ("Perulangan do-while ke-$i <br>");
        $i++;
    }   while ($i<= 10);
?>